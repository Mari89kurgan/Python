def addition(numbers_list):
    return int(numbers_list[0]) + int(numbers_list[1])


def subtraction(numbers_list):
    return int(numbers_list[0]) - int(numbers_list[1])


def multiplication(numbers_list):
    return int(numbers_list[0]) * int(numbers_list[1])


def division(numbers_list):
    return int(numbers_list[0]) / int(numbers_list[1])
